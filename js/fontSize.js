function changeSize(c){
    if(c=="s"){
        document.getElementById("neirong").style.fontSize = "12px";
    }
    else if(c==="m"){
        document.getElementById("neirong").style.fontSize = "16px";
    }
    else if(c==="b"){
        document.getElementById("neirong").style.fontSize = "26px";
    }
}    